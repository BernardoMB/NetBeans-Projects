public class Compra {
    
    // Atributos.
    private int tienda, tipo, cantidad;

    public Compra(int tienda, int tipo, int cantidad) {
        this.tienda = tienda;
        this.tipo = tipo;
        this.cantidad = cantidad;
    }

    public int getTienda() {
        return tienda;
    }

    public int getTipo() {
        return tipo;
    }

    public int getCantidad() {
        return cantidad;
    }
    
    
    
}
