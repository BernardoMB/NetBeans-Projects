    public int numeroSuerte(int d, int m, int a) {
        int suma, suerte;
        suerte = 0;
        suma = d + m + a;
        while (suma > 0) {
            suerte = suerte + suma % 10;
            suma = suma / 10;
        }
        return suerte;
    }
    
