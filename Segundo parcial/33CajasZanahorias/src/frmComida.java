    private void btnComerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComerActionPerformed
        String iniciales;
        int i, loc;
        iniciales = "";
        loc = 0;
        if (this.cmbComensales.getSelectedIndex() != -1){
            leeInfo(this.cmbComensales.getSelectedItem().toString());
            for (i = 0; i < cajas; i++)
                iniciales = iniciales + i + " - " + zanahorias[i] + "\n";
            this.txtaIniciales.setText(iniciales);
            for( i = quiereComer; i > 0; i--){
                loc  = this.cajaMasZanahorias();
                zanahorias[loc] = zanahorias[loc] - 1;
            }
            iniciales = "";
            for (i = 0; i < cajas; i++) {
                iniciales = iniciales + i + " - " + zanahorias[i] + "\n";
            }
            this.txtFinales.setText(iniciales);
            this.lblRespuesta.setText("ComiÃ³ " + quiereComer + " zanahorias y ultima de la caja " + loc);
        }
        else
            JOptionPane.showMessageDialog(null, "Seleccione un comensal por favor"
                    + "\n\nsi no desea comer haga clic en Terminar");
        
    }//GEN-LAST:event_btnComerActionPerformed
    
