    public void leeInfo(String nombre){
        File datos;
        Scanner lee;
        datos = new File(nombre + ".txt");
        zanahorias = new int[DIM];
        cajas = 0;
        int hay;
        hay = 0;
        try{
        lee = new Scanner(datos);
        }
        catch(Exception e){
            lee = null;
        }
        if (lee != null){
            this.quiereComer = lee.nextInt();
            while(lee.hasNextInt() && cajas < DIM){
                zanahorias[cajas] = Math.min(100, lee.nextInt());
                hay = hay + zanahorias[cajas];
                cajas = cajas + 1;
            }
            lee.close();
            this.quiereComer = Math.min(hay, quiereComer);
        }
        
    }
    
